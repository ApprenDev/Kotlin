fun main(args : Array<String>) {

    /*
       The rationals operators

      == : Returns true if the value on the left side is equal to the one on the right side.
      !=  : Returns true if the value on the left side is not equal to the one on the right side.
      <   : The true result if the value on the left side is less than the one on the right side.
      <= : Returns true if the value on the left side is less than or equal to the right side.
      >   : Returns true if the value on the left side is greater than the right side.
      >= : Returns true if the value on the left side is greater than or equal to the right side.

     */

    val firstValue = "Android"
    val secondValue = "Development"

    if (firstValue == secondValue) {
        println("The values are equals")

    } else {
        println("The values not are equals")
    }

}